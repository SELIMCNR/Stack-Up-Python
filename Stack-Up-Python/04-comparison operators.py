# == equals to
print(1907 == 1907)
print("Hello" == "Hello")

# != not equals to
print("hello" != "hello")
print("fenerbahçe" != "Fenerbahçe")

# > more than
print(1 > 2)

# >= more than OR equals to
print(2 >= 2)

# < less than
print(1 < 2)

# <= less than OR equals to
print(3 <= 2)
